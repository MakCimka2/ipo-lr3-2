num1 = input("Введите число 1: ") # Запрос числа 1
num2 = input("Введите число 2: ") # Запрос числа 2
if num1 == num2: # Проверка числа на четность
     print("Числа равны между собой") # Выводит Числа равны между собой
else:
     print(f"Меньшее число {min(num1, num2)}") # Выводит меньшее число